export function isItemAllowedForVariant(
	itemId: SettingItemId,
	isV2: boolean,
): boolean {
	const variant = SETTING_ITEM_VARIANT[itemId];
	if (variant === "shared") return true;
	return isV2 ? variant === "v2" : variant === "v1";
}

